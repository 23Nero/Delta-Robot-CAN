void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  HAL_UART_Receive_IT(&huart2, rx_buff, 24);
	if((sscanf((char*)rx_buff, "%da%db%dc%dd", &node1, &node2, &node3, &node4)) == 4)
	{
	TxHeader.DLC = 4;  // data length
  TxHeader.IDE = CAN_ID_STD;
  TxHeader.RTR = CAN_RTR_DATA;
  TxHeader.StdId = 0x002;  // ID
	
	TxData[0] = (uint8_t)node1;
	TxData[1] = (uint8_t)node2;
	TxData[2] = (uint8_t)node3;
	TxData[3] = (uint8_t)node4;
	
	HAL_CAN_AddTxMessage(&hcan1, &TxHeader, TxData, &TxMailbox);
	}

}

void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
	HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &RxHeader, RxData);
	if (RxHeader.StdId == 0x001)
	{
		flag_node1 = 1;
	}
	else if(RxHeader.StdId == 0x002)
	{
		flag_node2 = 1;
	}
	else if(RxHeader.StdId == 0x003)
	{
		flag_node3 = 1;
	}
	else if(RxHeader.StdId == 0x004)
	{
		flag_node4 = 1;
	}
	
	
}

